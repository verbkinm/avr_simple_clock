clock_mode:		.byte	1		; Храниние состояния отображения (время == 0, дата == 1)

;------------------------- Начало программного кода

.cseg		 					; Выбор сегмента программного кода
		.org	0				; Установка текущего адреса на ноль

start:	

.include "interrupts_vector.asm"
