;------------------------- Резервирование ячеек памяти

.dseg							; Выбираем сегмент ОЗУ
		.org	0x60			; Устанавливаем текущий адрес сегмента

d1:				.byte	1		; Хранение первого символа
d2:				.byte	1		; Хранение второго символа
